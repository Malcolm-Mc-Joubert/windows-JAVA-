    private void remplirTable() {

        try {
            DbConect = Connexion.getConnexion();
            PreparedStatement Prestate = DbConect.prepareStatement("SELECT * FROM article (id_article,titre_article,chapeau_article,resume_article,texte_article,date_parution_article,etat_article VALUE(?,?,?,?,?,?,?)");
            ResultSet rslt = Prestate.executeQuery();

            Object[] tLigne;

            DefaultTableModel dfmodel = (DefaultTableModel) jTableSupArticle.getModel();
            //pour vider les champ 
            dfmodel.getDataVector().removeAllElements();

            while (rslt.next()) {
                tLigne = new Object[2];

                tLigne[0] = rslt.getString(1);
                tLigne[1] = rslt.getString(2);

                dfmodel.addRow(tLigne);
            }

        } catch (Exception e) {
            jTextAreaMessage.setText(e.getMessage());
            System.out.println("Jusqu a la cava");
        }

    }


